    private async Task CommitSurveysAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingSurveyRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.Surveys.EntityType = "Survey";
        report.Surveys.Approved = approved.Count;

        if (approved.Count == 0) return;

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                if (!_idMap.TryGetValue(staging.OriginalBuildingId, out var productionBuildingId))
                    throw new InvalidOperationException(
                        $"Building {staging.OriginalBuildingId} not found in ID map.");

                Guid? productionPropertyUnitId = null;
                if (staging.OriginalPropertyUnitId.HasValue &&
                    _idMap.TryGetValue(staging.OriginalPropertyUnitId.Value, out var unitId))
                {
                    productionPropertyUnitId = unitId;
                }

                // Resolve field collector ID — verify it exists in Users table,
                // fall back to the importing user if the original collector is not found
                // (tablet user IDs from .uhc packages may not exist in production)
                var fieldCollectorId = userId;
                if (staging.OriginalFieldCollectorId.HasValue)
                {
                    var collector = await _unitOfWork.Users.GetByIdAsync(
                        staging.OriginalFieldCollectorId.Value, ct);
                    if (collector != null)
                        fieldCollectorId = collector.Id;
                    else
                        _logger.LogWarning(
                            "Field collector {CollectorId} not found in Users table for Survey {SurveyId}. " +
                            "Using importing user {UserId} as fallback.",
                            staging.OriginalFieldCollectorId.Value, staging.OriginalEntityId, userId);
                }

                var type = staging.Type ?? SurveyType.Field;
                var prefix = type == SurveyType.Office ? "OFC" : "ALG";
                var refCode = staging.ReferenceCode
                    ?? await _surveyRefCodeGenerator.GenerateNextAsync(prefix, ct);

                // Check if a survey with this ReferenceCode already exists in production
                // (e.g., from a previous package covering the same survey)
                if (refCode != null)
                {
                    var existingSurvey = await _unitOfWork.Surveys
                        .GetByReferenceCodeAsync(refCode, ct);

                    if (existingSurvey != null)
                    {
                        _idMap[staging.OriginalEntityId] = existingSurvey.Id;
                        staging.SetCommittedEntityId(existingSurvey.Id);
                        await _stagingSurveyRepo.UpdateAsync(staging, ct);

                        if (!await FlushRecordAsync("Survey", staging.Id, staging.OriginalEntityId,
                                existingSurvey.Id, report.Surveys, report, ct))
                            continue;

                        report.Surveys.Committed++;
                        report.Surveys.IdMappings[staging.OriginalEntityId] = existingSurvey.Id;

                        _logger.LogInformation(
                            "Survey {OriginalId} reused existing production survey {ProductionId} (ReferenceCode: {RefCode})",
                            staging.OriginalEntityId, existingSurvey.Id, refCode);
                        continue;
                    }
                }

                var survey = Survey.Create(
                    buildingId: productionBuildingId,
                    fieldCollectorId: fieldCollectorId,
                    type: type,
                    surveyDate: staging.SurveyDate,
                    propertyUnitId: productionPropertyUnitId,
                    createdByUserId: userId,
                    referenceCode: refCode);

                // Transfer survey details (GPS, notes, duration)
                survey.UpdateSurveyDetails(
                    staging.GpsCoordinates,
                    staging.Notes,
                    staging.DurationMinutes,
                    userId);

                // Resolve contact person from staging.
                // Production persons committed earlier in this transaction are not yet in the DB
                // (SaveChangesAsync runs only at the end), so GetByIdAsync would return null.
                // Use staging person data directly to build the full name snapshot.
                if (staging.OriginalContactPersonId.HasValue &&
                    _idMap.TryGetValue(staging.OriginalContactPersonId.Value, out var productionContactPersonId))
                {
                    var stagingContact = await _stagingPersonRepo.GetByPackageAndOriginalIdAsync(
                        importPackageId, staging.OriginalContactPersonId.Value, ct);

                    if (stagingContact != null)
                    {
                        var parts = new[] {
                            stagingContact.FirstNameArabic,
                            stagingContact.FatherNameArabic,
                            stagingContact.FamilyNameArabic
                        };
                        var fullName = string.Join(" ", parts.Where(p => !string.IsNullOrWhiteSpace(p)));
                        if (!string.IsNullOrWhiteSpace(stagingContact.MotherNameArabic))
                            fullName += $" ({stagingContact.MotherNameArabic})";
                        survey.SetContactPerson(productionContactPersonId, fullName, userId);
                    }
                }

                // Surveys arriving via .uhc are normally finalized on the tablet before export,
                // but Obstructed/Cancelled surveys must keep their incoming status — they were
                // never completed in the field and finalizing would erase that signal.
                if (staging.Status == SurveyStatus.Obstructed)
                {
                    survey.MarkAsObstructed(userId);
                }
                else if (staging.Status == SurveyStatus.Cancelled)
                {
                    survey.Cancel(userId);
                }
                else if (survey.ContactPersonId.HasValue)
                {
                    // MarkAsFinalized requires a contact person — only call it when one was resolved.
                    survey.MarkAsFinalized(userId);
                }

                await _unitOfWork.Surveys.AddAsync(survey, ct);

                _idMap[staging.OriginalEntityId] = survey.Id;
                staging.SetCommittedEntityId(survey.Id);
                await _stagingSurveyRepo.UpdateAsync(staging, ct);

                if (!await FlushRecordAsync("Survey", staging.Id, staging.OriginalEntityId,
                        survey.Id, report.Surveys, report, ct))
                    continue;

                report.Surveys.Committed++;
                report.Surveys.IdMappings[staging.OriginalEntityId] = survey.Id;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit Survey {OriginalId}", staging.OriginalEntityId);
                report.Surveys.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "Survey",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

    /// <summary>
    /// Commit claims to production with Claim Number generation (CL-YYYY-NNNNNN).
    /// Claim ID format: CL-YYYY-NNNNNN via IClaimNumberGenerator.
    /// </summary>
    private async Task CommitClaimsAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingClaimRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.Claims.EntityType = "Claim";
        report.Claims.Approved = approved.Count;

        if (approved.Count == 0) return;

        // Pre-load staging relations for CaseStatus resolution by RelationType
        var stagingRelations = await _stagingRelationRepo.GetApprovedForCommitAsync(importPackageId, ct);
        var relationByPersonAndUnit = stagingRelations
            .GroupBy(r => (r.OriginalPersonId, r.OriginalPropertyUnitId))
            .ToDictionary(g => g.Key, g => g.First());

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                if (!_idMap.TryGetValue(staging.OriginalPropertyUnitId, out var productionUnitId))
                    throw new InvalidOperationException(
                        $"PropertyUnit {staging.OriginalPropertyUnitId} not found in ID map.");

                // Resolve primary claimant — required for claim creation
                if (!staging.OriginalPrimaryClaimantId.HasValue)
                    throw new InvalidOperationException(
                        $"Staging claim {staging.OriginalEntityId} has no PrimaryClaimantId. Claims require a claimant.");

                if (!_idMap.TryGetValue(staging.OriginalPrimaryClaimantId.Value, out var productionClaimantId))
                    throw new InvalidOperationException(
                        $"Person {staging.OriginalPrimaryClaimantId.Value} not found in ID map for claim {staging.OriginalEntityId}.");

                // Generate a proper claim number
                var claimNumber = await _claimNumberGenerator.GenerateNextClaimNumberAsync(ct);

                // Resolve originating survey if present
                Guid? productionSurveyId = null;
                if (staging.OriginalOriginatingSurveyId.HasValue &&
                    _idMap.TryGetValue(staging.OriginalOriginatingSurveyId.Value, out var surveyId))
                {
                    productionSurveyId = surveyId;
                }

                var claim = Claim.Create(
                    claimNumber: claimNumber,
                    propertyUnitId: productionUnitId,
                    primaryClaimantId: productionClaimantId,
                    claimType: staging.ClaimType,
                    claimSource: staging.ClaimSource,
                    createdByUserId: userId,
                    originatingSurveyId: productionSurveyId);

                // Transfer tenure contract, ownership share, and description from staging
                if (staging.TenureContractType.HasValue)
                    claim.UpdateTenureContract(staging.TenureContractType.Value, null, userId);

                if (staging.OwnershipShare.HasValue)
                    claim.UpdateOwnershipShare((int)staging.OwnershipShare.Value, userId);

                if (!string.IsNullOrWhiteSpace(staging.ClaimDescription))
                    claim.UpdateClaimDescription(staging.ClaimDescription, userId);

                // Claims from tablet → set lifecycle to Submitted
                claim.Submit(userId, userId);

                // Set CaseStatus based on the generating PersonPropertyRelation's RelationType
                if (staging.OriginalPrimaryClaimantId.HasValue &&
                    relationByPersonAndUnit.TryGetValue(
                        (staging.OriginalPrimaryClaimantId.Value, staging.OriginalPropertyUnitId),
                        out var stagingRelation))
                {
                    if (stagingRelation.RelationType == RelationType.Owner ||
                        stagingRelation.RelationType == RelationType.Heir)
                    {
                        claim.CloseCase(userId);
                    }
                }

                await _unitOfWork.Claims.AddAsync(claim, ct);

                _idMap[staging.OriginalEntityId] = claim.Id;
                staging.SetCommittedEntityId(claim.Id);
                await _stagingClaimRepo.UpdateAsync(staging, ct);

                if (!await FlushRecordAsync("Claim", staging.Id, staging.OriginalEntityId,
                        claim.Id, report.Claims, report, ct))
                    continue;

                report.Claims.Committed++;
                report.Claims.IdMappings[staging.OriginalEntityId] = claim.Id;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit Claim {OriginalId}", staging.OriginalEntityId);
                report.Claims.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "Claim",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

    /// <summary>
    /// Commit evidence to production with SHA-256 attachment deduplication.
    /// Uses IEvidenceRepository.GetByFileHashAsync() for dedup lookup.
    /// </summary>
    private async Task CommitEvidenceAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingEvidenceRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.Evidences.EntityType = "Evidence";
        report.Evidences.Approved = approved.Count;

        if (approved.Count == 0) return;

        // Pre-load junction rows for many-to-many evidence-relation links (v1.8+)
        var allJunctionRows = await _stagingEvidenceRelationRepo.GetByPackageIdAsync(importPackageId, ct);
        var junctionByEvidence = allJunctionRows
            .GroupBy(r => r.OriginalEvidenceId)
            .ToDictionary(g => g.Key, g => g.ToList());

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                // Attachment deduplication by SHA-256 hash
                var filePath = staging.FilePath;
                if (!string.IsNullOrEmpty(staging.FileHash))
                {
                    // Check if evidence with this hash already exists in production
                    var existingEvidence = await _unitOfWork.Evidences
                        .GetByFileHashAsync(staging.FileHash, ct);

                    if (existingEvidence != null)
                    {
                        // Reuse existing file path — no need to store another copy
                        filePath = existingEvidence.FilePath;
                        report.DuplicateAttachmentsFound++;
                        report.DeduplicationBytesSaved += staging.FileSizeBytes;

                        _logger.LogInformation(
                            "Deduplicated evidence file {Hash} — reusing {ExistingPath}",
                            staging.FileHash, filePath);
                    }
                }

                var evidence = Evidence.Create(
                    evidenceType: staging.EvidenceType,
                    description: staging.Description,
                    originalFileName: staging.OriginalFileName,
                    filePath: filePath,
                    fileSizeBytes: staging.FileSizeBytes,
                    mimeType: staging.MimeType,
                    fileHash: staging.FileHash,
                    createdByUserId: userId);

                // Link evidence to person-property relations
                // Priority: junction table (many-to-many) > direct FK (legacy 1:1)
                junctionByEvidence.TryGetValue(staging.OriginalEntityId, out var junctionLinks);

                if (junctionLinks != null && junctionLinks.Count > 0)
                {
                    // v1.8+ path: create one EvidenceRelation per junction row
                    foreach (var link in junctionLinks)
                    {
                        if (_idMap.TryGetValue(link.OriginalPersonPropertyRelationId, out var prodRelId))
                        {
                            var evidenceRelation = EvidenceRelation.Create(
                                evidenceId: evidence.Id,
                                personPropertyRelationId: prodRelId,
                                linkedBy: userId);
                            await _unitOfWork.EvidenceRelations.AddAsync(evidenceRelation, ct);
                        }
                    }
                }
                else if (staging.OriginalPersonPropertyRelationId.HasValue &&
                    _idMap.TryGetValue(staging.OriginalPersonPropertyRelationId.Value, out var fallbackRelId))
                {
                    // Legacy fallback: direct FK on evidence row (v1.7 and earlier)
                    var evidenceRelation = EvidenceRelation.Create(
                        evidenceId: evidence.Id,
                        personPropertyRelationId: fallbackRelId,
                        linkedBy: userId);
                    await _unitOfWork.EvidenceRelations.AddAsync(evidenceRelation, ct);
                }

                if (staging.OriginalClaimId.HasValue &&
                    _idMap.TryGetValue(staging.OriginalClaimId.Value, out var prodClaimId))
                {
                    evidence.LinkToClaim(prodClaimId, userId);
                }

                await _unitOfWork.Evidences.AddAsync(evidence, ct);

                _idMap[staging.OriginalEntityId] = evidence.Id;
                staging.SetCommittedEntityId(evidence.Id);
                await _stagingEvidenceRepo.UpdateAsync(staging, ct);

                if (!await FlushRecordAsync("Evidence", staging.Id, staging.OriginalEntityId,
                        evidence.Id, report.Evidences, report, ct))
                    continue;

                report.Evidences.Committed++;
                report.Evidences.IdMappings[staging.OriginalEntityId] = evidence.Id;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit Evidence {OriginalId}", staging.OriginalEntityId);
                report.Evidences.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "Evidence",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

    /// <summary>
    /// Commit identification documents to production.
    /// </summary>
    private async Task CommitIdentificationDocumentsAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingIdDocRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.IdentificationDocuments.EntityType = "IdentificationDocument";
        report.IdentificationDocuments.Approved = approved.Count;

        if (approved.Count == 0) return;

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                // Resolve person ID
                if (!_idMap.TryGetValue(staging.OriginalPersonId, out var prodPersonId))
                {
                    _logger.LogWarning(
                        "IdentificationDocument {OriginalId} references unknown Person {PersonId}, skipping",
                        staging.OriginalEntityId, staging.OriginalPersonId);
                    report.IdentificationDocuments.Failed++;
                    continue;
                }

                var doc = IdentificationDocument.Create(
                    documentType: staging.DocumentType,
                    description: staging.Description,
                    originalFileName: staging.OriginalFileName,
                    filePath: staging.FilePath,
                    fileSizeBytes: staging.FileSizeBytes,
                    mimeType: staging.MimeType,
                    fileHash: staging.FileHash,
                    personId: prodPersonId,
                    createdByUserId: userId);

                if (staging.DocumentIssuedDate.HasValue || staging.DocumentExpiryDate.HasValue ||
                    !string.IsNullOrEmpty(staging.IssuingAuthority) || !string.IsNullOrEmpty(staging.DocumentReferenceNumber) ||
                    !string.IsNullOrEmpty(staging.Notes))
                {
                    doc.UpdateMetadata(
                        issuedDate: staging.DocumentIssuedDate,
                        expiryDate: staging.DocumentExpiryDate,
                        issuingAuthority: staging.IssuingAuthority,
                        referenceNumber: staging.DocumentReferenceNumber,
                        notes: staging.Notes,
                        modifiedByUserId: userId);
                }

                await _unitOfWork.IdentificationDocuments.AddAsync(doc, ct);

                _idMap[staging.OriginalEntityId] = doc.Id;
                staging.SetCommittedEntityId(doc.Id);
                await _stagingIdDocRepo.UpdateAsync(staging, ct);

                if (!await FlushRecordAsync("IdentificationDocument", staging.Id, staging.OriginalEntityId,
                        doc.Id, report.IdentificationDocuments, report, ct))
                    continue;

                report.IdentificationDocuments.Committed++;
                report.IdentificationDocuments.IdMappings[staging.OriginalEntityId] = doc.Id;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit IdentificationDocument {OriginalId}", staging.OriginalEntityId);
                report.IdentificationDocuments.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "IdentificationDocument",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

    /// <summary>
    /// Link Cases to committed entities. Runs as the last step so all IDs are resolved.
    /// For each unique PropertyUnit touched by this import:
    ///   - Creates a Case if one doesn't exist
    ///   - Links all committed Surveys, Claims, and PersonPropertyRelations to the Case
    ///   - Auto-closes the Case if an ownership/heir claim was committed
    /// </summary>
    private async Task LinkCasesAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        try
        {
            // Collect all production PropertyUnit IDs touched by this import
            // from committed surveys, claims, and relations
            var propertyUnitIds = new HashSet<Guid>();

            // From surveys
            var committedSurveys = await _stagingSurveyRepo.GetApprovedForCommitAsync(importPackageId, ct);
            foreach (var staging in committedSurveys)
            {
                if (staging.CommittedEntityId.HasValue && staging.OriginalPropertyUnitId.HasValue)
                {
                    if (_idMap.TryGetValue(staging.OriginalPropertyUnitId.Value, out var productionUnitId))
                        propertyUnitIds.Add(productionUnitId);
                }
            }

            // From claims
            var committedClaims = await _stagingClaimRepo.GetApprovedForCommitAsync(importPackageId, ct);
            foreach (var staging in committedClaims)
            {
                if (staging.CommittedEntityId.HasValue)
                {
                    if (_idMap.TryGetValue(staging.OriginalPropertyUnitId, out var productionUnitId))
                        propertyUnitIds.Add(productionUnitId);
                }
            }

            if (!propertyUnitIds.Any()) return;

            foreach (var propertyUnitId in propertyUnitIds)
            {
                try
                {
                    // Get or create Case for this PropertyUnit
                    var caseEntity = await _unitOfWork.Cases.GetByPropertyUnitIdAsync(propertyUnitId, ct);

                    if (caseEntity == null)
                    {
                        var caseNumber = await _caseNumberGenerator.GenerateNextCaseNumberAsync(ct);
                        caseEntity = Case.Create(caseNumber, propertyUnitId, userId);
                        await _unitOfWork.Cases.AddAsync(caseEntity, ct);
                        await _unitOfWork.SaveChangesAsync(ct);

                        _logger.LogInformation(
                            "Created Case {CaseNumber} for PropertyUnit {PropertyUnitId}",
                            caseNumber, propertyUnitId);
                    }

                    // Link surveys to Case
                    var surveys = await _unitOfWork.Surveys.GetByPropertyUnitAsync(propertyUnitId, ct);
                    foreach (var survey in surveys)
                    {
                        if (!survey.CaseId.HasValue)
                            survey.LinkToCase(caseEntity.Id, userId);
                    }

                    // Link claims to Case and check for ownership
                    var claims = await _unitOfWork.Claims.GetAllByPropertyUnitIdAsync(propertyUnitId, ct);
                    foreach (var claim in claims)
                    {
                        if (!claim.CaseId.HasValue)
                            claim.LinkToCase(caseEntity.Id, userId);

                        // Auto-close Case on ownership/heir claim
                        if (claim.ClaimType == ClaimType.OwnershipClaim)
                        {
                            caseEntity.Close(claim.Id, userId);
                            _logger.LogInformation(
                                "Case {CaseNumber} closed by ownership claim {ClaimNumber}",
                                caseEntity.CaseNumber, claim.ClaimNumber);
                        }
                    }

                    // Link PersonPropertyRelations to Case
                    var relations = await _unitOfWork.PersonPropertyRelations
                        .GetByPropertyUnitIdAsync(propertyUnitId, ct);
                    foreach (var relation in relations)
                    {
                        if (!relation.CaseId.HasValue)
                            relation.LinkToCase(caseEntity.Id, userId);
                    }

                    await _unitOfWork.SaveChangesAsync(ct);
                }
                catch (Exception ex)
                {
                    _logger.LogWarning(ex,
                        "Failed to link Case for PropertyUnit {PropertyUnitId}. Import continues.",
                        propertyUnitId);
                }
            }
        }
        catch (Exception ex)
        {
            _logger.LogWarning(ex, "Case linking failed for import {ImportPackageId}. Import data is unaffected.", importPackageId);
        }
    }

