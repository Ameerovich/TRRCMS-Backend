    private readonly IStagingRepository<StagingBuilding> _stagingBuildingRepo;
    private readonly IStagingRepository<StagingBuildingDocument> _stagingBuildingDocRepo;
    private readonly IStagingRepository<StagingPropertyUnit> _stagingPropertyUnitRepo;
    private readonly IStagingRepository<StagingPerson> _stagingPersonRepo;
    private readonly IStagingRepository<StagingHousehold> _stagingHouseholdRepo;
    private readonly IStagingRepository<StagingPersonPropertyRelation> _stagingRelationRepo;
    private readonly IStagingRepository<StagingEvidence> _stagingEvidenceRepo;
    private readonly IStagingRepository<StagingEvidenceRelation> _stagingEvidenceRelationRepo;
    private readonly IStagingRepository<StagingIdentificationDocument> _stagingIdDocRepo;
    private readonly IStagingRepository<StagingClaim> _stagingClaimRepo;
    private readonly IStagingRepository<StagingSurvey> _stagingSurveyRepo;
    private readonly IImportPackageRepository _importPackageRepository;
    private readonly IClaimNumberGenerator _claimNumberGenerator;
    private readonly ICaseNumberGenerator _caseNumberGenerator;
    private readonly ISurveyReferenceCodeGenerator _surveyRefCodeGenerator;
    private readonly IImportService _importService;
    private readonly IFileStorageService _fileStorageService;
    private readonly IGeometryConverter _geometryConverter;
    private readonly ImportPipelineSettings _settings;
    private readonly ILogger<CommitService> _logger;

    // ID mapping: OriginalEntityId (from .uhc) → Production Entity Id
    private readonly Dictionary<Guid, Guid> _idMap = new();

    // OriginalEntityIds of staging persons whose NationalId must be cleared before insert
    // (operator chose KeepSeparate on a NationalId conflict — unique DB constraint would block commit)
    private readonly HashSet<Guid> _keepSeparatePersonIds = new();

    // OriginalEntityIds of staging property units that must bypass the upsert path and force-insert
    // (operator chose KeepSeparate on a composite-key conflict — they are genuinely distinct units)
    private readonly HashSet<Guid> _keepSeparateUnitIds = new();

    // (BuildingId, UnitIdentifier) pairs already assigned to a unit earlier in the CURRENT commit
    // transaction. Records are flushed individually within savepoints, so a freshly-inserted unit is
    // visible to the DB on the next query — but tracking the assignment here is still cheaper and
    // avoids handing the same identifier to two units in one package (e.g. within-batch KeepSeparate
    // of two units sharing a building + identifier). Keyed "{buildingId}|{identifier}".
    private readonly HashSet<string> _assignedUnitIdentifiers = new();

    // Monotonic counter producing unique SAVEPOINT names within a single commit transaction.
    private int _savepointCounter;

