    /// <inheritdoc />
    public async Task<CommitReportDto> CommitAsync(
        Guid importPackageId,
        Guid committedByUserId,
        CancellationToken cancellationToken = default)
    {
        _idMap.Clear();
        _keepSeparatePersonIds.Clear();
        _keepSeparateUnitIds.Clear();
        _assignedUnitIdentifiers.Clear();
        _savepointCounter = 0;

        var report = new CommitReportDto
        {
            ImportPackageId = importPackageId,
            CommittedByUserId = committedByUserId,
            CommittedAtUtc = DateTime.UtcNow
        };

        var package = await _importPackageRepository.GetByIdAsync(importPackageId, cancellationToken)
            ?? throw new InvalidOperationException($"Package {importPackageId} not found.");

        report.PackageNumber = package.PackageNumber;

        // Pre-populate ID map from already-committed staging records (for re-commit scenarios).
        // When a partial commit succeeded, some staging entities already have CommittedEntityId set.
        // These won't be re-committed (GetApprovedForCommitAsync skips them), but child entities
        // (e.g. Claims referencing a PropertyUnit) still need their FK resolved via _idMap.
        await PrePopulateIdMapFromCommittedAsync(importPackageId, cancellationToken);

        // Load KeepSeparate decisions so commit can honour them per entity type.
        await LoadKeepSeparateDecisionsAsync(importPackageId, cancellationToken);

        // Clear change tracker to ensure no stale dirty entities from prior operations
        _unitOfWork.DetachAllEntities();

        // Execute the entire commit in a transaction.
        // The whole commit flushes with a single SaveChangesAsync, so a unique-constraint
        // violation surfaces here and rolls the entire package back. Translate the raw
        // Postgres error into an actionable message instead of leaking the SQLSTATE string.
        try
        {
            await _unitOfWork.ExecuteInTransactionAsync(async () =>
            {
                // Commit in FK-dependency order
                await CommitBuildingsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitBuildingDocumentsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitPropertyUnitsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await PopulateMergeRedirectsAsync("PropertyUnit", importPackageId, cancellationToken);
                await CommitPersonsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await PopulateMergeRedirectsAsync("Person", importPackageId, cancellationToken);
                await CommitHouseholdsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitPersonPropertyRelationsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitSurveysAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitClaimsAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitEvidenceAsync(importPackageId, committedByUserId, report, cancellationToken);
                await CommitIdentificationDocumentsAsync(importPackageId, committedByUserId, report, cancellationToken);

                // Link Cases — runs last so all entities are resolved
                await LinkCasesAsync(importPackageId, committedByUserId, report, cancellationToken);
            }, cancellationToken);
        }
        catch (DbUpdateException ex) when (ex.InnerException is PostgresException pg && pg.SqlState == "23505")
        {
            // Safety net: a unique violation that escaped per-record savepoint handling (e.g. raised
            // by the final flush or LinkCases) aborts the whole transaction. Surface it clearly.
            _logger.LogError(ex,
                "Commit for package {PackageId} rejected by a unique constraint ({Constraint}). Rolled back.",
                importPackageId, pg.ConstraintName);
            throw new ConflictException(
                TranslateCommitError("Package", pg.ConstraintName, pg.Detail, pg.Message), ex);
        }

        // Update aggregate counts
        report.TotalRecordsCommitted =
            report.BuildingDocuments.Committed + report.Buildings.Committed +
            report.PropertyUnits.Committed +
            report.Persons.Committed + report.Households.Committed +
            report.PersonPropertyRelations.Committed + report.Evidences.Committed +
            report.IdentificationDocuments.Committed +
            report.Claims.Committed + report.Surveys.Committed;

        report.TotalRecordsFailed =
            report.BuildingDocuments.Failed + report.Buildings.Failed +
            report.PropertyUnits.Failed +
            report.Persons.Failed + report.Households.Failed +
            report.PersonPropertyRelations.Failed + report.Evidences.Failed +
            report.IdentificationDocuments.Failed +
            report.Claims.Failed + report.Surveys.Failed;

        report.TotalRecordsApproved =
            report.BuildingDocuments.Approved + report.Buildings.Approved +
            report.PropertyUnits.Approved +
            report.Persons.Approved + report.Households.Approved +
            report.PersonPropertyRelations.Approved + report.Evidences.Approved +
            report.IdentificationDocuments.Approved +
            report.Claims.Approved + report.Surveys.Approved;

        report.Status = report.IsFullySuccessful
            ? ImportStatus.Completed.ToString()
            : (report.TotalRecordsCommitted > 0 ? ImportStatus.PartiallyCompleted.ToString() : ImportStatus.Failed.ToString());

        return report;
    }

    /// <inheritdoc />
    public async Task<string> ArchivePackageAsync(
        Guid importPackageId,
        Guid committedByUserId,
        CancellationToken cancellationToken = default)
    {
        var package = await _importPackageRepository.GetByIdAsync(importPackageId, cancellationToken)
            ?? throw new InvalidOperationException($"Package {importPackageId} not found.");

        // Resolve source path: prefer UploadedFilePath (set during upload), fallback to convention
        string? sourceFilePath = null;

        if (!string.IsNullOrWhiteSpace(package.UploadedFilePath) && File.Exists(package.UploadedFilePath))
        {
            sourceFilePath = package.UploadedFilePath;
        }
        else
        {
            // Fallback: search in package storage directory
            var storagePath = Path.GetFullPath(_settings.PackageStoragePath);
            if (Directory.Exists(storagePath))
            {
                sourceFilePath = Directory.GetFiles(storagePath, $"*{package.PackageId:N}*")
                    .FirstOrDefault();

                sourceFilePath ??= Directory.GetFiles(storagePath, $"*{Path.GetFileNameWithoutExtension(package.FileName)}*")
                    .FirstOrDefault();
            }
        }

        if (sourceFilePath == null || !File.Exists(sourceFilePath))
        {
            _logger.LogWarning(
                "Source .uhc file not found for package {PackageId}. " +
                "UploadedFilePath='{UploadedFilePath}', skipping archive.",
                package.PackageId, package.UploadedFilePath);
            return string.Empty;
        }

        var archivePath = await _importService.ArchivePackageAsync(
            sourceFilePath, package.PackageId, cancellationToken);

        return archivePath;
    }

    /// <summary>
    /// Populate _idMap with merge redirect mappings for resolved conflicts.
    ///
    /// When a conflict is resolved via Merge, the discarded entity's OriginalEntityId
    /// must map to the same production ID as the master entity. This ensures that
    /// other staging entities (e.g. StagingClaim referencing a merged StagingPropertyUnit)
    /// can resolve their FK references correctly during commit.
    ///
    /// Cross-batch: discarded is staging, master is production → _idMap[discarded] = production.Id
    /// Within-batch: discarded is staging, master is staging (already committed) → _idMap[discarded] = _idMap[master]
    /// </summary>

    /// <summary>
    /// Pre-populate the ID map from staging records that were already committed
    /// in a previous (partial) commit attempt. This ensures that child entities
    /// (e.g. Claims referencing an already-committed PropertyUnit) can still
    /// resolve their FK references via _idMap during re-commit.
    /// </summary>
    private async Task PrePopulateIdMapFromCommittedAsync(
        Guid importPackageId, CancellationToken ct)
    {
        var committedBuildingDocs = await _stagingBuildingDocRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var d in committedBuildingDocs)
            _idMap.TryAdd(d.OriginalEntityId, d.CommittedEntityId!.Value);

        var committedBuildings = await _stagingBuildingRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var b in committedBuildings)
            _idMap.TryAdd(b.OriginalEntityId, b.CommittedEntityId!.Value);

        var committedUnits = await _stagingPropertyUnitRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var u in committedUnits)
            _idMap.TryAdd(u.OriginalEntityId, u.CommittedEntityId!.Value);

        var committedPersons = await _stagingPersonRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var p in committedPersons)
            _idMap.TryAdd(p.OriginalEntityId, p.CommittedEntityId!.Value);

        var committedHouseholds = await _stagingHouseholdRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var h in committedHouseholds)
            _idMap.TryAdd(h.OriginalEntityId, h.CommittedEntityId!.Value);

        var committedRelations = await _stagingRelationRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var r in committedRelations)
            _idMap.TryAdd(r.OriginalEntityId, r.CommittedEntityId!.Value);

        var committedSurveys = await _stagingSurveyRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var s in committedSurveys)
            _idMap.TryAdd(s.OriginalEntityId, s.CommittedEntityId!.Value);

        var committedClaims = await _stagingClaimRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var c in committedClaims)
            _idMap.TryAdd(c.OriginalEntityId, c.CommittedEntityId!.Value);

        var committedEvidence = await _stagingEvidenceRepo.GetCommittedAsync(importPackageId, ct);
        foreach (var e in committedEvidence)
            _idMap.TryAdd(e.OriginalEntityId, e.CommittedEntityId!.Value);

        if (_idMap.Count > 0)
            _logger.LogInformation(
                "Pre-populated ID map with {Count} already-committed staging records for re-commit",
                _idMap.Count);
    }

    /// <summary>
    /// Find a UnitIdentifier that does not yet exist under <paramref name="buildingId"/> in production.
    /// Starts with the original value; appends -2, -3, … until a free slot is found.
    /// Used for KeepSeparate property unit conflicts where the operator confirmed the unit is distinct
    /// from an existing one with the same composite key.
    ///
    /// A candidate is "free" only if it collides with neither:
    ///   (a) any DB row — INCLUDING soft-deleted ones, because the unique index is unfiltered, so a
    ///       soft-deleted unit still reserves its (BuildingId, UnitIdentifier) slot; nor
    ///   (b) a unit already assigned earlier in this same commit transaction (buffered, not yet
    ///       flushed, so invisible to a DB query) — tracked in <see cref="_assignedUnitIdentifiers"/>.
    /// Both checks are required to avoid a unique-constraint violation at the final SaveChangesAsync.
    /// </summary>
    private async Task<string> FindAvailableUnitIdentifierAsync(
        Guid buildingId, string baseIdentifier, CancellationToken ct)
    {
        var candidate = baseIdentifier;
        var suffix = 2;
        while (_assignedUnitIdentifiers.Contains($"{buildingId}|{candidate}")
            || await _unitOfWork.PropertyUnits
                .GetByBuildingAndIdentifierIncludingDeletedAsync(buildingId, candidate, ct) != null)
        {
            candidate = $"{baseIdentifier}-{suffix++}";
        }
        return candidate;
    }

    /// <summary>
    /// Flush a single just-staged record inside its own SAVEPOINT. On success, the record's writes
    /// remain in the transaction. On a database rejection, the writes are rolled back, the in-memory
    /// ID-map / report bookkeeping for this record is undone (so FK-dependent records correctly skip
    /// it), and a clear per-record error is appended to the report. Returns whether the record committed.
    /// </summary>
    private async Task<bool> FlushRecordAsync(
        string entityType,
        Guid stagingId,
        Guid originalEntityId,
        Guid productionEntityId,
        CommitEntityTypeSummary section,
        CommitReportDto report,
        CancellationToken ct)
    {
        var savepoint = $"sp_{_savepointCounter++}";
        var result = await _unitOfWork.TrySaveChangesWithSavepointAsync(savepoint, ct);
        if (result.Success)
            return true;

        // Nothing was persisted for this record — undo bookkeeping so dependents skip it.
        // Remove from _idMap only if it still maps to THIS entity: some staging entities reuse
        // another entity's UUID (e.g. a Household reusing its PropertyUnit's id), and we must not
        // clobber that other entity's already-committed mapping.
        if (_idMap.TryGetValue(originalEntityId, out var mapped) && mapped == productionEntityId)
            _idMap.Remove(originalEntityId);
        section.IdMappings.Remove(originalEntityId);
        section.Failed++;
        report.Errors.Add(new CommitErrorDto
        {
            EntityType = entityType,
            StagingEntityId = stagingId,
            OriginalEntityId = originalEntityId,
            ErrorMessage = TranslateCommitError(entityType, result.ConstraintName, result.Detail, result.ErrorMessage)
        });
        _logger.LogWarning(
            "Commit rejected {EntityType} {OriginalId}: {Reason}",
            entityType, originalEntityId, result.ErrorMessage);
        return false;
    }

    /// <summary>
    /// Turn a database rejection into an operator-actionable, per-record message keyed off the
    /// violated constraint. Falls back to the raw detail for any constraint not special-cased.
    /// </summary>
    private static string TranslateCommitError(
        string entityType, string? constraintName, string? detail, string? rawMessage)
    {
        var constraint = constraintName ?? string.Empty;

        if (constraint.Contains("PropertyUnits", StringComparison.OrdinalIgnoreCase)
            && constraint.Contains("UnitIdentifier", StringComparison.OrdinalIgnoreCase))
        {
            return "Rejected: this property unit's identifier already exists in the building " +
                   "(BuildingId + UnitIdentifier must be unique). Resolve the property duplicate " +
                   $"(Merge or Keep-Separate) and re-commit. ({detail})";
        }

        if (constraint.Contains("NationalId", StringComparison.OrdinalIgnoreCase))
        {
            return "Rejected: this person's National ID already exists (must be unique). Resolve the " +
                   $"person duplicate (Merge or Keep-Separate, which clears the duplicate NID). ({detail})";
        }

        if (!string.IsNullOrEmpty(constraint))
            return $"Rejected by database rule '{constraint}'. {detail}";

        return $"Rejected: {rawMessage}";
    }

    /// <summary>
    /// Populate _keepSeparatePersonIds and _keepSeparateUnitIds from KeepSeparate-resolved conflicts.
    ///
    /// Person conflicts: NationalId unique constraint would block inserting a staging person that
    /// matches an existing production person by NationalId. Clearing the NationalId on the staging
    /// person before insert lets the record through; a data-quality reviewer can reconcile later.
    ///
    /// Property conflicts: The commit's upsert path would silently remap the staging unit to the
    /// existing production unit, ignoring the operator's KeepSeparate decision. Flagging the
    /// staging unit's OriginalEntityId bypasses the upsert and forces a new insert with a
    /// disambiguated UnitIdentifier suffix.
    ///
    /// Cross-batch: FirstEntityId is always the staging entity's OriginalEntityId.
    /// Within-batch: both FirstEntityId and SecondEntityId are staging OriginalEntityIds.
    /// </summary>
    private async Task LoadKeepSeparateDecisionsAsync(Guid importPackageId, CancellationToken ct)
    {
        var conflicts = await _unitOfWork.ConflictResolutions
            .GetResolvedKeepSeparateForPackageAsync(importPackageId, ct);

        foreach (var c in conflicts)
        {
            if (c.ConflictType.StartsWith("PersonDuplicate"))
            {
                _keepSeparatePersonIds.Add(c.FirstEntityId);
                if (c.ConflictType == "PersonDuplicate_WithinBatch")
                    _keepSeparatePersonIds.Add(c.SecondEntityId);
            }
            else if (c.ConflictType.StartsWith("PropertyDuplicate"))
            {
                _keepSeparateUnitIds.Add(c.FirstEntityId);
                if (c.ConflictType == "PropertyDuplicate_WithinBatch")
                    _keepSeparateUnitIds.Add(c.SecondEntityId);
            }
        }

        if (conflicts.Count > 0)
            _logger.LogInformation(
                "KeepSeparate decisions loaded: {PersonCount} persons, {UnitCount} property units",
                _keepSeparatePersonIds.Count, _keepSeparateUnitIds.Count);
    }

    private async Task PopulateMergeRedirectsAsync(
        string entityType, Guid importPackageId, CancellationToken ct)
    {
        var mergedConflicts = await _unitOfWork.ConflictResolutions
            .GetResolvedMergesForPackageAsync(importPackageId, entityType, ct);

        foreach (var conflict in mergedConflicts)
        {
            var masterId = conflict.MergedEntityId!.Value;
            var discardedId = conflict.DiscardedEntityId!.Value;

            // Skip if discarded is already in the map
            if (_idMap.ContainsKey(discardedId))
                continue;

            if (_idMap.TryGetValue(masterId, out var productionId))
            {
                // Within-batch: master was committed → redirect discarded to same production ID
                _idMap[discardedId] = productionId;
                _logger.LogDebug(
                    "Merge redirect (within-batch): {Discarded} → {Production} (via master {Master})",
                    discardedId, productionId, masterId);
            }
            else
            {
                // Cross-batch: masterId IS the production ID (not in _idMap because it wasn't staged)
                _idMap[discardedId] = masterId;
                _logger.LogDebug(
                    "Merge redirect (cross-batch): {Discarded} → {Production}",
                    discardedId, masterId);
            }
        }
    }

    /// <summary>
    /// Commit building documents (photos/PDFs) to production.
    /// FK to Buildings — committed after buildings so BuildingDocument.BuildingId can be resolved.
    /// Supports attachment deduplication by SHA-256 hash.
    /// </summary>
    private async Task CommitBuildingDocumentsAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingBuildingDocRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.BuildingDocuments.EntityType = "BuildingDocument";
        report.BuildingDocuments.Approved = approved.Count;

        if (approved.Count == 0) return;

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                // Attachment deduplication by SHA-256 hash
                var filePath = staging.FilePath;
                if (!string.IsNullOrEmpty(staging.FileHash))
                {
                    var existingDoc = await _unitOfWork.BuildingDocuments
                        .GetByFileHashAsync(staging.FileHash, ct);

                    if (existingDoc != null)
                    {
                        filePath = existingDoc.FilePath;
                        report.DuplicateAttachmentsFound++;
                        report.DeduplicationBytesSaved += staging.FileSizeBytes;

                        _logger.LogInformation(
                            "Deduplicated building document file {Hash} — reusing {ExistingPath}",
                            staging.FileHash, filePath);
                    }
                }

                // Resolve parent building via _idMap
                if (!_idMap.TryGetValue(staging.OriginalBuildingId, out var prodBuildingId))
                {
                    _logger.LogWarning(
                        "BuildingDocument {OriginalId} references unknown building {OriginalBuildingId} — skipping",
                        staging.OriginalEntityId, staging.OriginalBuildingId);
                    report.BuildingDocuments.Failed++;
                    continue;
                }

                var document = BuildingDocument.Create(
                    buildingId: prodBuildingId,
                    description: staging.Description,
                    originalFileName: staging.OriginalFileName,
                    filePath: filePath,
                    fileSizeBytes: staging.FileSizeBytes,
                    mimeType: staging.MimeType,
                    fileHash: staging.FileHash,
                    createdByUserId: userId);

                await _unitOfWork.BuildingDocuments.AddAsync(document, ct);

                _idMap[staging.OriginalEntityId] = document.Id;
                staging.SetCommittedEntityId(document.Id);
                await _stagingBuildingDocRepo.UpdateAsync(staging, ct);

                if (!await FlushRecordAsync("BuildingDocument", staging.Id, staging.OriginalEntityId,
                        document.Id, report.BuildingDocuments, report, ct))
                    continue;

                report.BuildingDocuments.Committed++;
                report.BuildingDocuments.IdMappings[staging.OriginalEntityId] = document.Id;

                _logger.LogDebug(
                    "Committed BuildingDocument {OriginalId} → {ProductionId}",
                    staging.OriginalEntityId, document.Id);
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit BuildingDocument {OriginalId}", staging.OriginalEntityId);
                report.BuildingDocuments.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "BuildingDocument",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

    /// <summary>
    /// Commit buildings to production.
    /// Building Record ID (BuildingId) is a 17-digit geographic hierarchy code
    /// composed automatically by Building.Create() from the administrative codes
    /// (GovernorateCode + DistrictCode + SubDistrictCode + CommunityCode +
    /// NeighborhoodCode + BuildingNumber). No external sequence needed.
    /// Building Record ID format: GG-DD-SS-CCC-NNN-BBBBB.
    /// </summary>
    private async Task CommitBuildingsAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingBuildingRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.Buildings.EntityType = "Building";
        report.Buildings.Approved = approved.Count;

        if (approved.Count == 0) return;

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                // Compose the 17-digit BuildingId from geographic codes
                var buildingIdCode = $"{staging.GovernorateCode}{staging.DistrictCode}" +
                    $"{staging.SubDistrictCode}{staging.CommunityCode}" +
                    $"{staging.NeighborhoodCode}{staging.BuildingNumber}";

                // Check if a building with this BuildingId already exists in production
                // (e.g., from a previous package covering the same building)
                var existingBuilding = await _unitOfWork.Buildings
                    .GetByBuildingIdAsync(buildingIdCode, ct);

                if (existingBuilding != null)
                {
                    // Skip update if building is locked — still map the ID for dependent entities
                    if (existingBuilding.IsLocked)
                    {
                        _logger.LogWarning(
                            "Building {BuildingId} is locked — skipping field survey update. Staging {OriginalId} mapped to existing {ProductionId}.",
                            buildingIdCode, staging.OriginalEntityId, existingBuilding.Id);

                        _idMap[staging.OriginalEntityId] = existingBuilding.Id;
                        staging.SetCommittedEntityId(existingBuilding.Id);
                        await _stagingBuildingRepo.UpdateAsync(staging, ct);

                        if (!await FlushRecordAsync("Building", staging.Id, staging.OriginalEntityId,
                                existingBuilding.Id, report.Buildings, report, ct))
                            continue;

                        report.Buildings.Committed++;
                        report.Buildings.IdMappings[staging.OriginalEntityId] = existingBuilding.Id;
                        continue;
                    }

                    // Update existing building with field survey data
                    // (preserves BuildingId, admin codes, geometry, and coordinates)
                    existingBuilding.UpdateFromFieldSurvey(
                        buildingType: staging.BuildingType,
                        status: staging.Status,
                        numberOfPropertyUnits: staging.NumberOfPropertyUnits,
                        numberOfApartments: staging.NumberOfApartments,
                        numberOfShops: staging.NumberOfShops,
                        notes: staging.Notes,
                        modifiedByUserId: userId);

                    await _unitOfWork.Buildings.UpdateAsync(existingBuilding, ct);

                    _idMap[staging.OriginalEntityId] = existingBuilding.Id;
                    staging.SetCommittedEntityId(existingBuilding.Id);
                    await _stagingBuildingRepo.UpdateAsync(staging, ct);

                    if (!await FlushRecordAsync("Building", staging.Id, staging.OriginalEntityId,
                            existingBuilding.Id, report.Buildings, report, ct))
                        continue;

                    report.Buildings.Committed++;
                    report.Buildings.IdMappings[staging.OriginalEntityId] = existingBuilding.Id;

                    _logger.LogInformation(
                        "Building {OriginalId} updated existing production building {ProductionId} (BuildingId: {BuildingId})",
                        staging.OriginalEntityId, existingBuilding.Id, buildingIdCode);
                    continue;
                }

                // No existing building — create a new one
                var building = Building.Create(
                    governorateCode: staging.GovernorateCode,
                    districtCode: staging.DistrictCode,
                    subDistrictCode: staging.SubDistrictCode,
                    communityCode: staging.CommunityCode,
                    neighborhoodCode: staging.NeighborhoodCode,
                    buildingNumber: staging.BuildingNumber,
                    governorateName: staging.GovernorateName ?? string.Empty,
                    districtName: staging.DistrictName ?? string.Empty,
                    subDistrictName: staging.SubDistrictName ?? string.Empty,
                    communityName: staging.CommunityName ?? string.Empty,
                    neighborhoodName: staging.NeighborhoodName ?? string.Empty,
                    buildingType: staging.BuildingType,
                    status: staging.Status,
                    createdByUserId: userId);

                building.UpdateUnitCounts(
                    staging.NumberOfPropertyUnits,
                    staging.NumberOfApartments,
                    staging.NumberOfShops,
                    userId);

                building.UpdateDetails(
                    staging.Notes,
                    userId);

                if (staging.BuildingGeometryWkt != null)
                {
                    var geometry = _geometryConverter.ParseWkt(staging.BuildingGeometryWkt);
                    building.SetGeometry(geometry, userId);
                }

                if (staging.Latitude.HasValue && staging.Longitude.HasValue)
                {
                    var fallbackPoint = _geometryConverter.CreatePoint(
                        (double)staging.Longitude.Value, (double)staging.Latitude.Value);
                    building.SetCoordinates(staging.Latitude.Value, staging.Longitude.Value, userId, fallbackPoint);
                }

                await _unitOfWork.Buildings.AddAsync(building, ct);

                _idMap[staging.OriginalEntityId] = building.Id;
                staging.SetCommittedEntityId(building.Id);
                await _stagingBuildingRepo.UpdateAsync(staging, ct);

                if (!await FlushRecordAsync("Building", staging.Id, staging.OriginalEntityId,
                        building.Id, report.Buildings, report, ct))
                    continue;

                report.Buildings.Committed++;
                report.Buildings.IdMappings[staging.OriginalEntityId] = building.Id;

                _logger.LogDebug(
                    "Committed Building {OriginalId} → {ProductionId} (BuildingId: {BuildingId})",
                    staging.OriginalEntityId, building.Id, building.BuildingId);
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit Building {OriginalId}", staging.OriginalEntityId);
                report.Buildings.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "Building",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

    /// <summary>
    /// Commit property units to production.
    /// Property Unit ID is derived from parent Building ID + UnitIdentifier.
    /// Full format = BuildingId-UnitNumber (e.g. 01-01-02-003-001-00001-001).
    /// </summary>
    private async Task CommitPropertyUnitsAsync(
        Guid importPackageId, Guid userId, CommitReportDto report, CancellationToken ct)
    {
        var approved = await _stagingPropertyUnitRepo.GetApprovedForCommitAsync(importPackageId, ct);
        report.PropertyUnits.EntityType = "PropertyUnit";
        report.PropertyUnits.Approved = approved.Count;

        if (approved.Count == 0) return;

        for (var i = 0; i < approved.Count; i++)
        {
            var staging = approved[i];
            try
            {
                // Resolve parent building FK
                if (!_idMap.TryGetValue(staging.OriginalBuildingId, out var productionBuildingId))
                {
                    throw new InvalidOperationException(
                        $"Parent Building {staging.OriginalBuildingId} not found in ID map. " +
                        "Building must be committed before its property units.");
                }

                // KeepSeparate: operator confirmed this is a genuinely distinct unit despite
                // sharing (BuildingId, UnitIdentifier) with an existing production unit.
                // Skip the upsert lookup entirely and force a new insert with a disambiguated identifier.
                var isKeepSeparate = _keepSeparateUnitIds.Contains(staging.OriginalEntityId);

                if (!isKeepSeparate)
                {
                    // Check if a LIVE property unit with this (BuildingId, UnitIdentifier) already
                    // exists (e.g., from a previous package covering the same building and unit).
                    var existingUnit = await _unitOfWork.PropertyUnits
                        .GetByBuildingAndIdentifierAsync(productionBuildingId, staging.UnitIdentifier, ct);

                    if (existingUnit != null)
                    {
                        // Reuse the existing production unit — just map the ID
                        _idMap[staging.OriginalEntityId] = existingUnit.Id;
                        staging.SetCommittedEntityId(existingUnit.Id);
                        await _stagingPropertyUnitRepo.UpdateAsync(staging, ct);
                        _assignedUnitIdentifiers.Add($"{productionBuildingId}|{staging.UnitIdentifier}");

                        if (!await FlushRecordAsync("PropertyUnit", staging.Id, staging.OriginalEntityId,
                                existingUnit.Id, report.PropertyUnits, report, ct))
                            continue;

                        report.PropertyUnits.Committed++;
                        report.PropertyUnits.IdMappings[staging.OriginalEntityId] = existingUnit.Id;

                        _logger.LogInformation(
                            "PropertyUnit {OriginalId} reused existing production unit {ProductionId} (Building: {BuildingId}, Unit: {UnitId})",
                            staging.OriginalEntityId, existingUnit.Id, productionBuildingId, staging.UnitIdentifier);
                        continue;
                    }

                    // No live occupant — but the unique index is unfiltered, so a SOFT-DELETED unit
                    // may still hold this slot. Inserting would hit a unique-constraint violation and
                    // roll back the whole package. Since this is not a KeepSeparate decision, the
                    // composite key means "same unit": restore the soft-deleted row and reuse it.
                    var deletedUnit = await _unitOfWork.PropertyUnits
                        .GetByBuildingAndIdentifierIncludingDeletedAsync(productionBuildingId, staging.UnitIdentifier, ct);

                    if (deletedUnit != null)
                    {
                        deletedUnit.Restore(userId);
                        await _unitOfWork.PropertyUnits.UpdateAsync(deletedUnit, ct);

                        _idMap[staging.OriginalEntityId] = deletedUnit.Id;
                        staging.SetCommittedEntityId(deletedUnit.Id);
                        await _stagingPropertyUnitRepo.UpdateAsync(staging, ct);
                        _assignedUnitIdentifiers.Add($"{productionBuildingId}|{staging.UnitIdentifier}");

                        if (!await FlushRecordAsync("PropertyUnit", staging.Id, staging.OriginalEntityId,
                                deletedUnit.Id, report.PropertyUnits, report, ct))
                            continue;

                        report.PropertyUnits.Committed++;
                        report.PropertyUnits.IdMappings[staging.OriginalEntityId] = deletedUnit.Id;

                        _logger.LogInformation(
                            "PropertyUnit {OriginalId} restored previously soft-deleted production unit {ProductionId} " +
                            "(Building: {BuildingId}, Unit: {UnitId}) instead of inserting a colliding row",
                            staging.OriginalEntityId, deletedUnit.Id, productionBuildingId, staging.UnitIdentifier);
                        continue;
                    }
                }

                // KeepSeparate: find a unique identifier by appending a numeric suffix
                var unitIdentifier = staging.UnitIdentifier;
                if (isKeepSeparate)
                {
                    unitIdentifier = await FindAvailableUnitIdentifierAsync(
                        productionBuildingId, staging.UnitIdentifier, ct);
                    _logger.LogInformation(
                        "KeepSeparate: inserting staging PropertyUnit {OriginalId} as new unit with identifier '{Identifier}' (original: '{Original}')",
                        staging.OriginalEntityId, unitIdentifier, staging.UnitIdentifier);
                }

                // No existing unit (or KeepSeparate) — create a new one
                var unit = PropertyUnit.Create(
                    buildingId: productionBuildingId,
                    unitIdentifier: unitIdentifier,
                    unitType: staging.UnitType,
                    floorNumber: staging.FloorNumber,
                    createdByUserId: userId,
                    status: staging.Status,
                    numberOfRooms: staging.NumberOfRooms,
                    areaSquareMeters: staging.AreaSquareMeters,
                    description: staging.Description);

                // If KeepSeparate appended a suffix, preserve the original identifier so the
                // adjustment is reviewable rather than silently changing the field.
                if (isKeepSeparate && !string.Equals(unitIdentifier, staging.UnitIdentifier, StringComparison.Ordinal))
                {
                    unit.MarkUnitIdentifierAdjustedForKeepSeparate(staging.UnitIdentifier, userId);
                }

                await _unitOfWork.PropertyUnits.AddAsync(unit, ct);

                _idMap[staging.OriginalEntityId] = unit.Id;
                staging.SetCommittedEntityId(unit.Id);
                await _stagingPropertyUnitRepo.UpdateAsync(staging, ct);
                _assignedUnitIdentifiers.Add($"{productionBuildingId}|{unitIdentifier}");

                if (!await FlushRecordAsync("PropertyUnit", staging.Id, staging.OriginalEntityId,
                        unit.Id, report.PropertyUnits, report, ct))
                    continue;

                report.PropertyUnits.Committed++;
                report.PropertyUnits.IdMappings[staging.OriginalEntityId] = unit.Id;
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Failed to commit PropertyUnit {OriginalId}", staging.OriginalEntityId);
                report.PropertyUnits.Failed++;
                report.Errors.Add(new CommitErrorDto
                {
                    EntityType = "PropertyUnit",
                    StagingEntityId = staging.Id,
                    OriginalEntityId = staging.OriginalEntityId,
                    ErrorMessage = ex.Message
                });
            }
        }
    }

